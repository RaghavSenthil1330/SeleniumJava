package selenium.basic.Learning;

public class MondayProblem {

	public static void main(String[] args) {
		char pass='P',fail='F';
		
		char result='P';
		if(result == pass)
		{
			System.out.println("The student is passed");
		}
		else if(result== fail)
		{
			System.out.println("The student is failed");
		}
		
		else
		{
			System.out.println("Invalid input in result");
		}
		

	}

}
